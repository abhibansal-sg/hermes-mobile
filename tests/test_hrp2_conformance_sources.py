"""Guard the one-source HRP/2 schema and authenticated fixture."""

from __future__ import annotations

import hashlib
import json
from copy import deepcopy
from pathlib import Path

import pytest
from jsonschema import Draft202012Validator
from jsonschema.exceptions import ValidationError
from hermes_relay.v2.protocol import SecureMessage


ROOT = Path(__file__).resolve().parents[1]
PROTOCOL = ROOT / "protocol" / "hrp2"


def _load(name: str) -> dict:
    return json.loads((PROTOCOL / name).read_text(encoding="utf-8"))


def _validator(definition: str) -> Draft202012Validator:
    schema = _load("schema.json")
    return Draft202012Validator({
        "$schema": schema["$schema"],
        "$defs": schema["$defs"],
        "$ref": f"#/$defs/{definition}",
    })


def test_shared_schema_is_valid_draft_2020_12() -> None:
    Draft202012Validator.check_schema(_load("schema.json"))


def test_every_schema_integer_ceiling_is_exact_in_double_backed_json() -> None:
    maximum_safe_integer = (1 << 53) - 1
    schema = _load("schema.json")
    assert schema["$defs"]["uint64"]["maximum"] == maximum_safe_integer

    def maxima(value):
        if isinstance(value, dict):
            for key, child in value.items():
                if key == "maximum" and isinstance(child, int):
                    yield child
                yield from maxima(child)
        elif isinstance(value, list):
            for child in value:
                yield from maxima(child)

    assert all(value <= maximum_safe_integer for value in maxima(schema))


def test_frozen_authenticated_fixture_matches_shared_schema_and_hash() -> None:
    fixture = _load("fixtures/auth-envelope.json")
    _validator("outerEnvelope").validate(fixture["outer_envelope"])
    _validator("secureMessage").validate(fixture["inner_message"])
    encoded = json.dumps(
        fixture["outer_envelope"],
        ensure_ascii=False,
        sort_keys=True,
        separators=(",", ":"),
    ).encode("utf-8")
    assert (
        hashlib.sha256(encoded).hexdigest() == fixture["canonical_envelope_sha256_hex"]
    )


