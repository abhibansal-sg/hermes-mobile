def test_pairing_and_inner_wire_objects_reject_unknown_fields() -> None:
    offer = {
        "v": 2,
        "hub": "https://relay.example",
        "relay_route": "rte_agent",
        "offer_route": "off_mailbox",
        "offer_id": "ofr_fixture",
        "offer_transport_token": "d" * 43,
        "expires_at_ms": 1784450000000,
        "relay_kem_pub": "e" * 43,
        "relay_sign_pub": "f" * 43,
        "pair_secret": "g" * 43,
    }
    _validator("pairingOffer").validate(offer)
    with pytest.raises(ValidationError):
        _validator("pairingOffer").validate({**offer, "gateway_token": "forbidden"})

    pair_init = {
        "v": 2,
        "offer_id": "ofr_fixture",
        "device_name": "Fixture iPhone",
        "device_kem_pub": "a" * 43,
        "device_sign_pub": "b" * 43,
        "preview_kem_pub": "c" * 43,
        "device_nonce": "n" * 22,
        "push_bind_token": None,
        "hub_activation_token": None,
        "pair_mac": "m" * 43,
    }
    _validator("pairInit").validate(pair_init)
    with pytest.raises(ValidationError):
        _validator("pairInit").validate({**pair_init, "push_bind_token": ""})

    stream_ack = {
        "v": 2,
        "mid": "ABEiM0RVZneImaq7zN3u_w",
        "kind": "stream_ack",
        "sender_key_generation": 1,
        "created_at_ms": 1784449900000,
        "expires_at_ms": 1784450000000,
        "body": {"stream_id": "str_fixture", "through_seq": 7},
    }
    _validator("secureMessage").validate(stream_ack)
    malformed = deepcopy(stream_ack)
    malformed["body"]["session_id"] = "not_allowed"
    with pytest.raises(ValidationError):
        _validator("secureMessage").validate(malformed)


def test_rpc_and_approval_action_shapes_are_exact() -> None:
    request = {
        "jsonrpc": "2.0",
        "id": "rpc_fixture",
        "method": "prompt.submit",
        "params": {
            "text": "hello",
            "client_message_id": "018f47a0-63f2-7c64-8f16-b78e42c35db9",
        },
        "op_id": "op_fixture",
    }
    _validator("rpcRequest").validate(request)
    without_op = dict(request)
    without_op.pop("op_id")
    with pytest.raises(ValidationError):
        _validator("rpcRequest").validate(without_op)
    malformed_id = deepcopy(request)
    malformed_id["params"]["client_message_id"] = "msg_fixture_123456"
    with pytest.raises(ValidationError):
        _validator("rpcRequest").validate(malformed_id)
    unknown_param = deepcopy(request)
    unknown_param["params"]["gateway_token"] = "forbidden"
    with pytest.raises(ValidationError):
        _validator("rpcRequest").validate(unknown_param)

    boundary = deepcopy(request)
    boundary["id"] = "r" * 128
    boundary["op_id"] = "o" * 128
    boundary["params"]["session_id"] = "s" * 256
    _validator("rpcRequest").validate(boundary)
    for path, invalid in (
        (("id",), "r" * 129),
        (("op_id",), "short"),
        (("params", "session_id"), "s" * 257),
    ):
        malformed = deepcopy(boundary)
        target = malformed
        for component in path[:-1]:
            target = target[component]
        target[path[-1]] = invalid
        with pytest.raises(ValidationError):
            _validator("rpcRequest").validate(malformed)

    action = _load("fixtures/notification-preview.json")["preview"]["action"]
    _validator("approvalAction").validate(action)
    with pytest.raises(ValidationError):
        _validator("approvalAction").validate({
            **action,
            "gateway_url": "https://forbidden",
        })


def test_stream_integer_boundaries_match_exact_cross_runtime_json() -> None:
    batch = {
        "stream_id": "str_fixture",
        "first_seq": 1,
        "frames": [
            {
                "sid": "session_fixture",
                "turn": "turn_fixture",
                "kind": "turn.started",
                "body": {},
            }
        ],
    }
    _validator("frameBatch").validate(batch)
    _validator("frameBatch").validate({**batch, "first_seq": (1 << 53) - 1})
    for invalid in (0, 1 << 53):
        with pytest.raises(ValidationError):
            _validator("frameBatch").validate({**batch, "first_seq": invalid})

    checkpoint = {
        "stream_id": "str_fixture",
        "through_seq": 0,
        "session_id": "session_fixture",
        "snapshot_revision": 0,
        "replace": True,
        "items": [],
        "tombstones": [],
    }
    _validator("checkpoint").validate(checkpoint)
    with pytest.raises(ValidationError):
        _validator("checkpoint").validate({**checkpoint, "through_seq": 1 << 53})
