def test_python_consumes_fixture_emitted_by_production_swift_wire_types() -> None:
    """Consume bytes generated by RelayV2SecureMessage.canonicalJSON().

    The macOS iOS workflow compiles
    ``tests/fixtures/hrp2/SwiftProducedFixture.swift`` together with the
    production JSONValue.swift and RelayV2Protocol.swift, then byte-compares
    its output with this fixture. Linux CI consumes those checked-in bytes
    without pretending to run Apple frameworks or the iOS app.
    """

    fixture_dir = ROOT / "tests" / "fixtures" / "hrp2"
    wire = (fixture_dir / "swift-produced-secure-message.json").read_bytes().strip()
    decoded = json.loads(wire)
    _validator("secureMessage").validate(decoded)

    message = SecureMessage.from_bytes(wire)
    assert message.kind.value == "stream_ack"
    assert message.sender_key_generation == 7
    assert message.body == {
        "stream_id": "str_swift_produced",
        "through_seq": 17,
    }
    # Python's canonical encoder reproduces the exact bytes emitted by the
    # production Swift canonical encoder, including whole-number JSON syntax.
    assert message.to_bytes() == wire


